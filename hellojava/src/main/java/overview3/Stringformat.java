package overview3;

public class Stringformat {
    public static void main(String[] args) {
        System.out.printf("%20s", "FirstName");
        System.out.printf("%20s", "LastName");
        System.out.println();
        System.out.printf("%-20s", "FirstName");
        System.out.printf("%-20s", "LastName");
        System.out.println();
        System.out.printf("%s", "FirstName");
        System.out.printf("%s", "LastName");
    }
}
